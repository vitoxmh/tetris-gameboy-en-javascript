ctx = null;

constructor(ctx){


this.ctx = ctx;
    


}



clearRender(dx,dy,sprites){

    sprites.forEach((row,y) => {

        row.forEach((value,x) => {

            if(value !== 0){
                this.ctx.fillStyle = "#fff";
                this.ctx.fillRect((dx + (x * this.width)),(dy + (y * this.width)),this.width,this.width);  
            }

        });
    });

}



renderSprite(dx,dy,sprites){

    sprites.forEach((row,y) => {

        row.forEach((value,x) => {

            if(value !== 0){
                this.ctx.fillStyle = this.color[value];
                this.ctx.fillRect((dx + (x * this.width)),(dy + (y * this.width)),this.width,this.width);  
            }

        });
    });

}


