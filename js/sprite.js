renderPiece(matrizPiece,dx,dy){

    matrizPiece.forEach((row,y) => {

        row.forEach((value,x) => {

            if(value !== 0){
                this.ctx.fillStyle = this.color[value];
                this.ctx.fillRect((dx + (x * this.width)),(dy + (y * this.width)),this.width,this.width);  
            }

        });
    });


}



rotarSprite(matriz){
	
    return matriz[0].map((val, index) => matriz.map(row => row[index]).reverse());
    
}
    
renderNumber(number,x,y){

    const numberString = number.toString().split("").reverse().join("");

    var dx = 0;



    for(var i = 0; i < numberString.length; i++){

        if(numberString[i] == 1){


            dx = 34;

        }else{

            dx = 36;

        }



        this.renderSprite(x - (i*dx), y,this.get(numberString[i]));
    }


    

}



rederScore(score,name,posName,postScore){


    if(score != null){

        for(var i = 0; i < name.toString().length; i++){

            var dx = ((41*i)+posName.x);
            this.ctx.fillStyle ="red";
            this.ctx.fillRect((dx),posName.y+10,41,10);
            this.renderSprite(dx,posName.y,this.get(name[i]));
 
        }

        const firstPlaceScore = score.toString();

        for(var i = firstPlaceScore.length; i > 0; i--){

            var dx = (postScore.x-(33*i));
            this.ctx.fillStyle ="red";
            this.ctx.fillRect((dx),postScore.y,38,16);
           
 
        }

        this.renderNumber(firstPlaceScore,(postScore.x-20),(posName.y));

    }



    
}


